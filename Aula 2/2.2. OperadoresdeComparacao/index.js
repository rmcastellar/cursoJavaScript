let num1 = 5;
let num2 = 10;

console.log("Igualdade: " + (num1) + " == " + (num2) + " = " + (num1 == num2)); // Igualdade de valor, compara string com valor
console.log("Desigualdade: " + (num1) + " != " + (num2) + " = " + (num1 != num2)); // Desigualdade de valor, compara string com valor
console.log("Maior que: " + (num1) + " > " + (num2) + " = " + (num1 > num2)); // Maior que
console.log("Menor que: " + (num1) + " < " + (num2) + " = " + (num1 < num2)); // Menor que
console.log("Maior ou igual a: " + (num1) + " >= " + (num2) + " = " + (num1 >= num2)); // Maior ou igual a
console.log("Menor ou igual a: " + (num1) + " <= " + (num2) + " = " + (num1 <= num2));
console.log("Estritamente igual: " + (num1 === num2)); // Estritamente iguais tipo e valor
console.log("Estritamente diferente: " + (num1 !== num2)); // Estritamente diferentes tipo e valor